EOF

print_success "Deployment summary created: deployment-summary.md"

# Optional: Deploy to platforms if CLI tools are available
if command -v vercel >/dev/null 2>&1; then
    print_status "Vercel CLI found. Deploy with: vercel --prod"
else
    print_warning "Vercel CLI not found. Install with: npm i -g vercel"
fi

if command -v netlify >/dev/null 2>&1; then
    print_status "Netlify CLI found. Deploy with: netlify deploy --prod"
else
    print_warning "Netlify CLI not found. Install with: npm i -g netlify-cli"
fi

print_success "Website validation complete!"
print_status "Ready for deployment to hosting platforms"

