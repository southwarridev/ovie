#!/bin/bash

# Ovie Programming Language - Website Deployment Script
# Deploys the Ovie website to multiple hosting platforms

set -e

echo "ðŸŒ Ovie Website Deployment Script v2.0.0"
echo "=========================================="

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m' # No Color

# Function to print colored output
print_status() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

print_success() {
    echo -e "${GREEN}[SUCCESS]${NC} $1"
}

print_warning() {
    echo -e "${YELLOW}[WARNING]${NC} $1"
}

print_error() {
    echo -e "${RED}[ERROR]${NC} $1"
}

# Check if website directory exists
if [ ! -d "website" ]; then
    print_error "Website directory not found!"
    exit 1
fi

print_status "Validating website files..."

# Check required files
required_files=(
    "website/index.html"
    "website/assets/styles.css"
    "website/assets/script.js"
    "website/assets/ovie.png"
    "website/assets/ovie-icon.svg"
)

for file in "${required_files[@]}"; do
    if [ ! -f "$file" ]; then
        print_error "Required file missing: $file"
        exit 1
    fi
done

print_success "All required files found!"

# Validate HTML
print_status "Validating HTML structure..."
if command -v tidy >/dev/null 2>&1; then
    tidy -q -e website/index.html || print_warning "HTML validation warnings found"
else
    print_warning "HTML Tidy not found, skipping HTML validation"
fi

# Check for SEO essentials
print_status "Checking SEO essentials..."
if grep -q "meta name=\"description\"" website/index.html; then
    print_success "Meta description found"
else
    print_error "Meta description missing!"
    exit 1
fi

if grep -q "application/ld+json" website/index.html; then
    print_success "Structured data found"
else
    print_warning "Structured data missing"
fi

# Check for Ovie branding
print_status "Checking Ovie branding..."
if grep -q "ovie.png" website/index.html; then
    print_success "Ovie logo found in HTML"
else
    print_error "Ovie logo missing from HTML!"
    exit 1
fi

# Check for self-hosted references
print_status "Checking self-hosted references..."
if grep -q "Self-Hosted" website/index.html; then
    print_success "Self-hosted references found"
else
    print_error "Self-hosted references missing!"
    exit 1
fi

# Check deployment configurations
print_status "Checking deployment configurations..."

if [ -f "vercel.json" ]; then
    print_success "Vercel configuration found"
    # Validate JSON
    if command -v jq >/dev/null 2>&1; then
        jq empty vercel.json && print_success "Vercel config is valid JSON"
    fi
else
    print_warning "Vercel configuration missing"
fi

if [ -f "netlify.toml" ]; then
    print_success "Netlify configuration found"
else
    print_warning "Netlify configuration missing"
fi

# Create deployment summary
print_status "Creating deployment summary..."
cat > deployment-summary.md << EOF
# Ovie Website Deployment Summary

**Date**: $(date)
**Version**: 2.0.0 (Self-Hosted)
**Status**: Ready for deployment

## Files Validated
- âœ… HTML structure
- âœ… CSS assets
